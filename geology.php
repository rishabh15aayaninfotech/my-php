            <h3>Birbal Sahni’s Contribution to Indian Geology (By S.R. Narayana Rao, Palaeobotanist 1: 46-48)</h3>
            <div class="divider"></div>
            <div class="row">
              <div class="col-md-12">
                <p class="normal-text">For more than three decades, since the completion of Feistmantel's classic work
                  on the Indian Gondwana flora in 1886, the study of fossil plants had suffered a serious set-back as
                  Indian geologists were sceptical regarding their value in geological chronology. Geologists were
                  largely influenced by the attitude which W. T. Blanford took up in 1876 that the evidence founded upon
                  fossil plants should be received with caution, and that such evidence was in some cases opposed to the
                  evidence furnished by marine faunas. The year 1920, in which Seward and Sahni's volume on the revision
                  of Indian Gondwana plants appeared, is a landmark in the history of Indian geology and palaeobotany:
                  this year marks the revival of palaeobotanical research in India with plant fossils coming more and
                  more into the picture of Indian geology. Prof. Sahni was a rare combination of the botanist and the
                  geologist and the unique position he held in both the sciences made him eminently suited to bridge the
                  gulf that separated them. He did more than anyone else to convince the geologist that study of plant
                  fossils yielded results of a far-reaching nature which the geologist could not afford to ignore.</p>
                <p class="normal-text">To Prof. Sahni plant fossils were not just chance relics of ancient floras; they
                  had a deeper significance to him. Their geological background and implications were always present in
                  his mind. His work, at every stage, impinged upon the domain of geology, and the field of palaeobotany
                  became a meeting ground for botanists and geologists in this country. In a memorable address delivered
                  to geologists in 1926, he expressed that fossil plants represent the debt that botany owes to geology.
                  In return, palaeobotanical research which he initiated has not only been helpful in solving
                  stratigraphical problems, but has also thrown light upon questions of palaeogeography, past climates
                  and even earth movements. At the same time it has made its contributions to economic geology.</p>
                <p class="normal-text">It is difficult to give an adequate idea, within the space of a brief article, of
                  the extent to which he influenced geological thought and research in India within the last two decades
                  and more. It was in this country that the first Glossopteris was discovered and the great problems in
                  geology concerning the Gondwanaland were raised and discussed. The Gondwana problems naturally
                  attracted a good deal of his attention. Two other chapters of Indian geology where his researches had
                  their repercussions were the Deccan Traps and the Punjab Saline Series. He realized the importance of
                  micropalaeontology, both in its academic and applied aspects, and the micropalaeontological technique
                  which he had already employed in his work on the Saline Series was extended to other problems: in
                  elucidating the Tertiary sequence of Assam and as an aid to the measurement of geological time in
                  India.</p>
                <p class="normal-text">Feistmantel's original classification into Upper, Middle (with Parsora stage as a
                  transitional stage) and Lower has been questioned by later geologists, particularly Cotter and Fox.
                  Fox thought there was no justification for Middle Gondwanas as there was a floral break above the
                  Panchet stage. He further included the Parsora stage in the Jurassic. Prof. Sahni, however, did not
                  agree that the Parsora flora was Jurassic and, on the other hand, he thought it was not younger than
                  the Trias and possibly as old as the Permian. The geologists, again, considered the upper age limit of
                  the Gondwanas as Lower Cretaceous, as Lower Cretaceous ammonites are found associated with the
                  east-coast Gondwanas. In this connection the silicified flora of the Rajmahals received a good deal of
                  attention from Prof. Sahni. Feistmantel's account of this flora was mostly confined to leaf
                  impressions and in recent years many fossil-bearing localities had been discovered. From a critical
                  examination of the petrifactions, Prof. Sahni came to the conclusion that the flora was Jurassic with
                  not a single species characteristic of the Cretaceous.</p>
                <p class="normal-text">A problem in which he took a great interest for many years was that of
                  continental drift. Whereas Wegener thought that continents had broken up by drifting apart, Prof.
                  Sahni, on palaeobotanical evidence, elaborated a complementary theory that continents once separated
                  by oceans had drifted towards each other.

                  In 1934 his first contribution on the silicified flora of the Deccan Intertrappean beds appeared, and
                  with this was revived a geological controversy which dates back to the time of the pioneer geologists
                  Hislop and Hunter. As against the Cretaceous age put forward on geological grounds by Blanford and
                  others, Prof. Sahni found that the flora was distinctly Eocene, and it is gratifying to note that the
                  Eocene view later received its strongest support from the geologists themselves.</p>
                <p class="normal-text">For more than sixty years, the age of the Saline Series had been a baffling
                  problem to Indian geologists and the way in which Prof. Sahni was attracted to this problem may be
                  stated in his own words (1947): “About four years ago, while with a party of students in the Salt Mine
                  at Khewra, it occurred to the author to dissolve a little of the saline earth and to examine some
                  drops of the brine under the microscope. The idea was that since the salt must have been formed from
                  sea-water by the drying up of a bay or lagoon, the brine ought to show at least some minute traces of
                  organic remains which might give a clue to its geological age. The surmise proved to be correct: quite
                  a number of little shreds of woody tissue of dicotyledons and conifers, as well as the chitinous
                  remains of winged insects, were discovered. These fragments had no doubt been washed into the water or
                  wafted on to its surface by the wind; and it was clear that if these creatures were alive at the time
                  the sea existed, the salt could not possibly be so old: as the Cambrian”. This view necessitated the
                  introduction of an overthrust between, the Saline Series and the overlying Cambrian beds. Dr. Gee and
                  other field geologists, however, maintain that the Saline Series of (the Salt Range is in its normal
                  stratigraphical sequence and, therefore, Pre-Cambrian in age. The critical evidence proving the Saline
                  Series to be the lowest exposed member of the Salt Range Cambrian is the nature of the junction of the
                  series and the overlying Cambrian beds. This junction, according to Dr. Gee, is an undisturbed
                  sedimentary one and the problem, therefore, is to discover how Prof. Sahni's microfossils were
                  introduced into the Cambrian sequence. To Dr. Gee's arguments, Prof. Sahni replied (1947): "Enough has
                  been said to show that the field criteria upon which reliance is placed by the geologists of the
                  Cambrian school are not safe criteria. The Salt Range question which has so long baffled us is no
                  longer a problem of local significance: we must learn to judge it by standards based upon wider
                  experience... Between the testimony of the rocks and the testimony of the fossils there can be no real
                  conflict. When the two do not seem to agree, it is the direct evidence of the fossils that is to be
                  relied upon: palaeontology is a surer foundation for stratigraphy than field evidence."One aspect of
                  geology in which he was particularly interested in later years was micropalaeontology regarding which
                  he states: "The last few decades have seen the rise of micropalaeontology to a position of
                  considerable importance in geology, particularly in the quest for oil. Although it was the academic
                  geologists who first realized the scientific value of microfossils, we owe the main developments in
                  this field to the applied geologists, and particularly to the palaeontologists employed in the oil and
                  coal industries."Regarding further applications of this branch of study, he writes: "We now know that
                  not all sedimentary formations which outwardly appeared to be unfossiliferous are really devoid of
                  organic remains. Some of these have recently been shown to be astonishingly rich in micro- fossils
                  representing both plant and animal groups. The Saline Series in the Salt Range of the Punjab is a good
                  case in point, so also the glacial tillites at the base of the Gondwana system in Australia and South
                  Africa - and quite recently organic remains have also been detected in the Talchir Boulder Bed near
                  Chittidil in the Salt Range. Owing to their wide dissemination in the body of the rock-matrix
                  microfossils can sometimes provide an age index even if small bits of the rock collected at random are
                  analyzed. ...There are great areas in India, particularly in the Peninsula, covered by ancient
                  sedimentary rocks of unknown or disputed age. Very few megafossils have been found in these strata,
                  nor are we likely to find many more in the future. An attempt may usefully be made to recover
                  microfossils from samples of these rocks which should be collected from localities and horizons by
                  geologists who best know the areas."
                  His activities were, however, not confined to the laboratory; he believed that palaeobotanists should
                  have experience of field-work and missed no opportunity of visiting fossil localities with his hammer,
                  note-book and Leica. The Salt Range, the Rajmahal Hills and the Deccan Intertrappean areas were all
                  familiar grounds to him. His field-notes of the Salt Range (preserved among his unpublished
                  manuscripts) bear testimony to his keen and shrewd perception and understanding of complicated
                  geological structures. Those who accompanied him in his geological excursions retain vivid memories of
                  a personality full of physical and mental vigour, never sparing himself and with an unbounded
                  enthusiasm for fossil collections and field data. A few weeks before his death he led "an excursion to
                  the Rajmahal Hills. Those who were with him can never forget the thrill and joy with which he greeted
                  the discovery of the Williamsonia-bearing beds near Amarjola. Among several projects he had planned
                  for the Institute of Palaeobotany, mapping of the plant-beds of India held a high priority. He was
                  also anxious to lead an expedition to the Spiti region of the Himalayas.</p>
                <p class="normal-text">He took very great interest in geological research and teaching in Indian
                  universities and it was due to his efforts that the subject was introduced in many of the
                  universities. He was the Head of the Geology Department of the Lucknow University since its inception
                  in 1943 : his inspiring lectures on dynamical geology and palaeobotany and the singular success he
                  achieved in stimulating and training young talent for research soon made the Department an important
                  teaching and research center for geology in this country.</p>
                <p class="normal-text">He had close friendships and strong scientific connections with leading
                  geologists allover the world, and no geologist from any part of the world while visiting India ever
                  missed the opportunity of meeting Prof. Sahni in his laboratory and museum at Lucknow. The voluminous
                  scientific correspondence he has left behind forms a very valuable record of contemporary geological
                  thought and trends.
                  No notice of Prof. Sahni's work or life would be complete without a reference to his wife, Srimati
                  Savitri Sahni, whose understanding sympathy and companionship meant everything to him. She always
                  accompanied him on his scientific travels and took part in many of his geological excursions. Her
                  unflinching devotion in no small measure contributed to the great scientific achievements of Prof.
                  Sahni.</p>
