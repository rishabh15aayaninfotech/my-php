                <p class="normal-text">
                  Described for the first time in 2005 (Alimohammadian et al. 2005), amber nodules discovered from the
                  Gujarat Lignites Mines of the Cambay and Kutch Basins, are unique in terms of the information they
                  provide for Indian geosciences. They occur at a time when India was drifting northwards towards Asia
                  and when global temperatures were influenced by a series of thermal events, raising global mean annual
                  temperatures by 50 to 80 C from current values of 150C. They record a unique biota that was
                  diversifying in the greenhouse earth at the time, these forms include a variety of plants, insects,
                  arthropods and ostracodes preserved in three dimensional excellence. Biochemical analysis carried out
                  by IIT Mumbai suggests that these ambers are polymerized tree resins that exuded from Dipterocarpaceae
                  (Sal trees) (Dutta et al., 2009, 2011, 2014, Dutta and Mallick, 2017). The Indian amber deposits have
                  commanded global attention since they are one of the few that occur during the Lower Eocene in an
                  equatorial climatic zone. In addition, high-resolution techniques have been developed whereby it is
                  possible to extract the fossil inclusions and study them by sophisticated instrumentations, such as
                  scanning electron microscopy (Rust et al., 2010), confocal laser scanning microscopy (Cai et al.,
                  2018, Fu et al., 2021), synchroton x-ray imaging (Stebner et al., 2016) and biochemistry by mass
                  spectroscopy methods (Beimforde et al. 2011).
