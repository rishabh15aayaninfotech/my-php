    function slideLeft() {
      if (currentIndex > 0) {
        currentIndex--;
      } else {
        // Handle the case when at the start and need to jump to the cloned items
        sliderContainer.style.transition = "none";
        currentIndex = sliderItems.length; // Jump to the cloned end
